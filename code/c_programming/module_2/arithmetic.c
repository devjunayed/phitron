#include <stdio.h>

int main()
{
    int a = 10;
    int b = 5;
    int sum = a + b;
    printf("summation = %d\n", sum);
    int sub = a - b;
    printf("subtraction = %d\n", sub);
    int mul = a * b;
    printf("multiplication = %d\n", mul);
    int division = a / b;
    printf("division = %d\n", division);
    return 0;
}